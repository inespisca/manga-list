function init(){
    for (var i = 0; i < manga.length; i++) {
        document.getElementById('side_list').innerHTML += "<li class='manga_name' onclick='show(" + i + ")'>" + manga[i].title + "</li>";
    }
}

init();

function show(i){
    document.getElementById('title').innerHTML = manga[i].title;
    document.getElementById('picture').innerHTML = "<img src='" + manga[i].pic +"' class='picture'>";
    document.getElementById('chapter').innerHTML = manga[i].chapter;
    document.getElementById('volume').innerHTML = manga[i].volume;
    document.getElementById('status').innerHTML = manga[i].status;
}

show(0);